PROMPT_TEMPLATE = """
Ты — помощник для генерации синтетических данных для службы поддержки.
Твоя задача: создать {count} примеров обращений клиентов на русском языке.

Категории: Payment, Technical, Delivery, Account, Other.
Приоритеты: High, Medium, Low.

Требования:
1. Тексты должны быть разнообразными (от 5 до 50 слов).
2. Распределение по классам должно быть равномерным.
3. Добавь немного опечаток и разговорных выражений (5-10% данных).
4. Формат вывода: CSV (text, category, priority).

Примеры:
- "У меня не проходит оплата картой, очень срочно!" -> Payment, High
- "Где мой заказ? Уже 5 дней жду" -> Delivery, Medium
- "Спасибо за быстрый ответ" -> Other, Low

Сгенерируй данные в формате CSV:
"""

# Функция для генерации промпта с нужным количеством примеров
def generate_prompt(count=100):
    """
    Генерирует финальный промпт для LLM.
    
    Args:
        count: количество примеров для генерации
    
    Returns:
        str: готовый промпт
    """
    return PROMPT_TEMPLATE.format(count=count)


# Пример использования
if __name__ == "__main__":
    # Генерируем промпт для 100 примеров
    prompt = generate_prompt(count=100)
    print(prompt)
    
    # Можно сохранить в файл для удобства
    with open("prompt_for_llm.txt", "w", encoding="utf-8") as f:
        f.write(prompt)
    
    print("\n✅ Промпт сохранён в файл 'prompt_for_llm.txt'")